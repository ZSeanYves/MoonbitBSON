export function validateSchema(schema) {
  const typeIdentifier = /^[A-Z][A-Za-z0-9_]*$/;
  const fieldIdentifier = /^[a-z][A-Za-z0-9_]*$/;
  if (!Array.isArray(schema.types) || schema.types.length === 0) {
    throw new Error("schema.types must be a non-empty array");
  }
  for (const type of schema.types) {
    if (!type || !typeIdentifier.test(type.name)) {
      throw new Error("each type.name must be a PascalCase identifier");
    }
    if (!Array.isArray(type.fields) || type.fields.length === 0) {
      throw new Error(`${type.name}: fields must be a non-empty array`);
    }
    const seen = new Set();
    for (const field of type.fields) {
      if (!field || !fieldIdentifier.test(field.name) || seen.has(field.name)) {
        throw new Error(`${type.name}: invalid or duplicate field ${field?.name}`);
      }
      seen.add(field.name);
      const wireName = field.rename ?? field.name;
      if (typeof wireName !== "string" || wireName.includes("\0")) {
        throw new Error(`${type.name}.${field.name}: invalid BSON field name`);
      }
      if (
        typeof field.type !== "string" ||
        !/^[A-Za-z][A-Za-z0-9_]*(?:\[[A-Za-z0-9_, ?]+\])?[?]?$/.test(field.type)
      ) {
        throw new Error(`${type.name}.${field.name}: invalid MoonBit type ${field.type}`);
      }
      if (field.optional === true && field.type.endsWith("?")) {
        throw new Error(`${type.name}.${field.name}: optional fields must not repeat '?'`);
      }
    }
  }
}

export function generateBsonImpls(schema, sourceDescription) {
  validateSchema(schema);
  const lines = [
    "// Generated by MoonbitBSON codegen. Do not edit.",
    `// Source: ${sourceDescription}`,
    "",
  ];
  for (const type of schema.types) {
    lines.push("///|");
    lines.push(`pub impl ToBson for ${type.name} with fn to_bson(self) {`);
    lines.push("  Document(");
    lines.push("    Document::new()");
    for (const [index, field] of type.fields.entries()) {
      const wireName = field.rename ?? field.name;
      const suffix = index === type.fields.length - 1 ? "," : "";
      lines.push(`    .set(${JSON.stringify(wireName)}, to_bson(self.${field.name}))${suffix}`);
    }
    lines.push("  )");
    lines.push("}");
    lines.push("");
    lines.push("///|");
    lines.push(`pub impl FromBson for ${type.name} with fn from_bson(value) {`);
    lines.push("  let document = Document::from_bson(value)");
    lines.push("  {");
    for (const field of type.fields) {
      const wireName = field.rename ?? field.name;
      if (field.optional === true) {
        lines.push(
          `    ${field.name}: document.get(${JSON.stringify(wireName)}).map(value => from_bson(value)),`,
        );
      } else {
        lines.push(
          `    ${field.name}: from_bson(document.require(${JSON.stringify(wireName)})),`,
        );
      }
    }
    lines.push("  }");
    lines.push("}");
    lines.push("");
  }
  return `${lines.join("\n").trimEnd()}\n`;
}
