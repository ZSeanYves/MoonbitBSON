import fs from "node:fs";
import path from "node:path";
import url from "node:url";

const root = path.resolve(path.dirname(url.fileURLToPath(import.meta.url)), "..");
const corpusDir = path.join(root, "testdata", "bson-corpus");
const outputPath = path.join(root, "src", "full_corpus_test.mbt");

function moonString(value) {
  let output = '"';
  for (const character of value) {
    const code = character.codePointAt(0);
    if (character === '"') output += '\\"';
    else if (character === "\\") output += "\\\\";
    else if (character === "\n") output += "\\n";
    else if (character === "\r") output += "\\r";
    else if (character === "\t") output += "\\t";
    else if (code >= 0x20 && code <= 0x7e) output += character;
    else output += `\\u{${code.toString(16)}}`;
  }
  return output + '"';
}

function option(value) {
  return value === undefined ? "None" : `Some(${moonString(value)})`;
}

const files = fs
  .readdirSync(corpusDir)
  .filter((name) => name.endsWith(".json"))
  .sort();

const valid = [];
const decodeErrors = [];
const decimalErrors = [];
const extendedJsonErrors = [];

for (const file of files) {
  const data = JSON.parse(fs.readFileSync(path.join(corpusDir, file), "utf8"));
  for (const [index, item] of (data.valid ?? []).entries()) {
    valid.push({
      id: `${file} valid ${index}: ${item.description}`,
      canonicalBson: item.canonical_bson,
      canonicalExtjson: item.canonical_extjson,
      relaxedExtjson: item.relaxed_extjson,
      degenerateBson: item.degenerate_bson,
      degenerateExtjson: item.degenerate_extjson,
      lossy: item.lossy === true,
    });
  }
  for (const [index, item] of (data.decodeErrors ?? []).entries()) {
    decodeErrors.push({
      id: `${file} decode ${index}: ${item.description}`,
      bson: item.bson,
    });
  }
  for (const [index, item] of (data.parseErrors ?? []).entries()) {
    const target = data.bson_type === "0x13" ? decimalErrors : extendedJsonErrors;
    target.push({
      id: `${file} parse ${index}: ${item.description}`,
      value: item.string,
    });
  }
}

const lines = [];
lines.push("// Generated by tools/generate-corpus-tests.mjs. Do not edit.");
lines.push("// Source: MongoDB BSON Corpus via bson-rust 3.1.0.");
lines.push("");
lines.push("///|");
lines.push("priv struct FullCorpusValid {");
lines.push("  id : String");
lines.push("  canonical_bson : String");
lines.push("  canonical_extjson : String");
lines.push("  relaxed_extjson : String?");
lines.push("  degenerate_bson : String?");
lines.push("  degenerate_extjson : String?");
lines.push("  lossy : Bool");
lines.push("}");
lines.push("");
lines.push("///|");
lines.push("priv struct FullCorpusError {");
lines.push("  id : String");
lines.push("  value : String");
lines.push("}");
lines.push("");
lines.push("///|");
lines.push("let full_corpus_valid : Array[FullCorpusValid] = [");
for (const item of valid) {
  lines.push("  {");
  lines.push(`    id: ${moonString(item.id)},`);
  lines.push(`    canonical_bson: ${moonString(item.canonicalBson)},`);
  lines.push(`    canonical_extjson: ${moonString(item.canonicalExtjson)},`);
  lines.push(`    relaxed_extjson: ${option(item.relaxedExtjson)},`);
  lines.push(`    degenerate_bson: ${option(item.degenerateBson)},`);
  lines.push(`    degenerate_extjson: ${option(item.degenerateExtjson)},`);
  lines.push(`    lossy: ${item.lossy},`);
  lines.push("  },");
}
lines.push("]");

function emitErrors(name, values) {
  lines.push("");
  lines.push("///|");
  lines.push(`let ${name} : Array[FullCorpusError] = [`);
  for (const item of values) {
    lines.push(`  { id: ${moonString(item.id)}, value: ${moonString(item.value)} },`);
  }
  lines.push("]");
}

emitErrors("full_corpus_decode_errors", decodeErrors.map((item) => ({ ...item, value: item.bson })));
emitErrors("full_corpus_decimal_errors", decimalErrors);
emitErrors("full_corpus_extended_json_errors", extendedJsonErrors);

lines.push(String.raw`
///|
fn full_corpus_hex(value : String) -> Bytes raise {
  let bytes : Array[Byte] = []
  for index in 0..<(value.length() / 2) {
    let high = full_corpus_hex_nibble(value[index * 2])
    let low = full_corpus_hex_nibble(value[index * 2 + 1])
    bytes.push((high * 16 + low).to_byte())
  }
  Bytes::from_array(bytes)
}

///|
fn full_corpus_hex_nibble(value : UInt16) -> Int raise {
  match value {
    '0'..='9' => value.to_int() - '0'.to_int()
    'a'..='f' => value.to_int() - 'a'.to_int() + 10
    'A'..='F' => value.to_int() - 'A'.to_int() + 10
    _ => fail("invalid generated corpus hex")
  }
}

///|
fn full_corpus_decode_fails(value : Bytes) -> Bool {
  try decode(value) catch {
    _ => true
  } noraise {
    _ => false
  }
}

///|
fn full_corpus_decimal_fails(value : String) -> Bool {
  try Decimal128::from_string(value) catch {
    _ => true
  } noraise {
    _ => false
  }
}

///|
fn full_corpus_extended_json_fails(value : String) -> Bool {
  try Document::from_extended_json_string(value) catch {
    _ => true
  } noraise {
    _ => false
  }
}

///|
fn full_corpus_check_valid(case : FullCorpusValid) -> Unit raise {
  let canonical = full_corpus_hex(case.canonical_bson)
  let document = decode(canonical)
  assert_eq(encode(document), canonical, msg=case.id)
  assert_eq(RawDocument::from_bytes(canonical).bytes(), canonical, msg=case.id)
  let from_canonical = Document::from_extended_json_string(
    case.canonical_extjson,
  )
  let normalized_canonical_json = from_canonical.to_extended_json()
  assert_eq(
    document.to_extended_json(),
    normalized_canonical_json,
    msg=case.id,
  )
  if !case.lossy {
    assert_eq(encode(from_canonical), canonical, msg=case.id)
  }
  if case.degenerate_bson is Some(degenerate_bson) {
    assert_eq(
      encode(decode(full_corpus_hex(degenerate_bson))),
      canonical,
      msg=case.id,
    )
  }
  if case.degenerate_extjson is Some(degenerate_extjson) {
    assert_eq(
      Document::from_extended_json_string(degenerate_extjson)
      .to_extended_json(),
      normalized_canonical_json,
      msg=case.id,
    )
  }
  if case.relaxed_extjson is Some(relaxed_extjson) {
    let normalized_relaxed_json = Document::from_extended_json_string(
      relaxed_extjson,
    ).to_relaxed_extended_json()
    assert_eq(
      document.to_relaxed_extended_json(),
      normalized_relaxed_json,
      msg=case.id,
    )
    assert_eq(
      Document::from_extended_json_string(relaxed_extjson)
      .to_relaxed_extended_json(),
      normalized_relaxed_json,
      msg=case.id,
    )
  }
}

///|
test "full MongoDB BSON Corpus valid vectors" {
  for case in full_corpus_valid {
    full_corpus_check_valid(case) catch {
      _ => fail(case.id)
    }
  }
}

///|
test "full MongoDB BSON Corpus decode errors" {
  for case in full_corpus_decode_errors {
    assert_true(
      full_corpus_decode_fails(full_corpus_hex(case.value)),
      msg=case.id,
    )
  }
}

///|
test "full MongoDB BSON Corpus Decimal128 parse errors" {
  for case in full_corpus_decimal_errors {
    assert_true(full_corpus_decimal_fails(case.value), msg=case.id)
  }
}

///|
test "full MongoDB BSON Corpus Extended JSON parse errors" {
  for case in full_corpus_extended_json_errors {
    assert_true(full_corpus_extended_json_fails(case.value), msg=case.id)
  }
}
`);

fs.writeFileSync(outputPath, `${lines.join("\n")}\n`);
console.log(
  `generated ${valid.length} valid, ${decodeErrors.length} decode, ` +
    `${decimalErrors.length} decimal parse, ${extendedJsonErrors.length} Extended JSON parse cases`,
);
